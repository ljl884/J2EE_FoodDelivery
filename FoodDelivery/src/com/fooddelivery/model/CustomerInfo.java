package com.fooddelivery.model;

import org.hibernate.annotations.Entity;

@Entity
public class CustomerInfo {
	private int id;
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
